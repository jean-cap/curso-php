<?php
/*
 * Operadores aritiméticos
 */

$a = 10;
$b = 2;

echo '<h1>Operadores aritiméticos</h1>';

echo 'Valor inicial de $a = ' . $a . '<br>';
echo 'Valor inicial de $b = ' . $b . '<br>';

echo '<br><br>';

// Soma:
echo 'Soma: ';
echo $a + $b;
echo '<hr>';

// Subtração:
echo 'Subtração: ';
echo $a - $b;
echo '<hr>';

// Multiplicação:
echo 'Multiplicação: ';
echo $a * $b;
echo '<hr>';

// Divisão:
echo 'Divisão: ';
echo $a / $b;
echo '<hr>';

// Módulo
echo 'Módulo: ';
echo $a % $b;
echo '<hr>';

// Potenciação:
echo 'Potenciação: ';
echo $a ** $b;
echo '<hr>';
