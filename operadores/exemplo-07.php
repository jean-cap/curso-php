<?php
/*
 * Operadores de incremento e decremento
 */
$a = 10;
$b = 10;

// Pós incremento:
echo $a++; // Retorna o valor e depois incrementa a variável => 10
echo '<br>';
echo $a; // => 11

echo '<br><br>';

// Pré incremento
echo ++$b; // Incrementa a variável e depois retorna o valor => 11
echo '<br>';
echo $b; // => 11
